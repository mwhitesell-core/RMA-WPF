				if (Reader.Read())
					TotalItemCount = (int)Reader[0];
				if (TotalItemCount == 0)
				{
					return new ObservableCollection<TRANSFER_FILE>();
				}

            }

            Reader = CoreReader("[SEQUENTIAL].[sp_TRANSFER_FILE_Search]", parameters);
            var collection = new ObservableCollection<TRANSFER_FILE>();

            while (Reader.Read())
            {
                collection.Add(new TRANSFER_FILE
                {
					RowCheckSum = Convert.ToInt32(Reader["RowCheckSum"]),
					ROWID = (Guid)Reader["ROWID"],
